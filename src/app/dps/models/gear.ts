export function getGearInternalStat(gearStatus: GearStatus): InternalStat {
  let internalStat = InternalStat({});
  for (const [slot, state] of Object.entries(gearStatus)) {
    internalStat = addInternalStat(
      internalStat,
      gearData[state.grade][slot as GearSlot][state.upgrade]
    );
  }
  return internalStat;
}
